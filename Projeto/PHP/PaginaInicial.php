            Gestão de Sistemas Informaticos à distância
 

            Este programa prepara aos diretores para integrar o ambiente de una empresa com a capacidade mais moderna que proporcionam os computadores, o software e as redes de informática. Os estudantes aprendem a interagir e aplicar a tecnologia da informação para resolver os problemas de um negócio e otimizar seu funcionamento. Este programa cobre os aspectos fundamentais da empresa e da computação, como os sistemas operativos, o panorama windows, a Internet, as redes de computadores e as aplicações de software mais habituais que podem ser utilizadas para aumentar o rendimento de um negocio.
            
             
            
